package day5;

public class Switch {
	
	public static void main(String[] args) {
		
		int a = 20;
		
		switch(a) {
		case 10:
			System.out.println("the values is 10");
			break;
		case 20:
			System.out.println("the values is 20");
			break;
		default :
			System.out.println("the value is not present");
		}
			
		
	}

}
