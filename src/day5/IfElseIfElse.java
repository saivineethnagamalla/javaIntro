package day5;

public class IfElseIfElse {

	public static void main(String[] args) {

		int a = 30;
		int b = 20;

		if (a == b) {
			System.out.println("a is equal to b");
		} else if (a < b) {
			System.out.println("a is smaller than b");
		} else {
			System.out.println("b is smaller than a");
		}

	}

}
