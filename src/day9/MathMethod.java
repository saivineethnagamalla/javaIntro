package day9;

public class MathMethod {

	public static void main(String[] args) {

		Integer h = -20;

		System.out.println(Math.abs(h)); //absolute value
		System.out.println(Math.floor(20.98)); // largest integer
		System.out.println(Math.ceil(20.98));// smallest integer 
		System.out.println(Math.round(20.98)); // integer that is closest
		System.out.println(Math.round(20.18));
		System.out.println(Math.sqrt(20.98));
		System.out.println(Math.log(20.98));
		System.out.println(Math.pow(2,4));
		System.out.println(Math.sin(45));
		System.out.println(Math.cos(45));
		System.out.println(Math.tan(45));
		System.out.println(Math.PI);
		System.out.println(Math.asin(Math.sin(45))); // Inverse function of the sine
		System.out.println(Math.asin(45));
		System.out.println(Math.acos(45)); // Inverse function of the cos
		System.out.println(Math.atan(45));// Inverse function of the tan
		
		System.out.println(Math.toRadians(45));
		System.out.println(Math.toDegrees(45));
		System.out.println(Math.random());
		
		
		
		
		
	}

}
