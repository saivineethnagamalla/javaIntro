package day6;

public class RelationalOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println("The value of A is in start " + a);
		System.out.println("The value of B is in start " + b);

		if (a == b) {
			System.out.println("Both the number are equal");
		}
		if (a != b) {
			System.out.println("Both the number are not equal");
		}
		if (a > b) {
			System.out.println("A is bigger than b");
		}
		if (a < b) {
			System.out.println("A is less than b");
		}
		if (a >= b) {
			System.out.println("A is bigger or equal to b");
		}
		if (a <= b) {
			System.out.println("A is less or equal to b");
		}

	}

}
